#include <stdio.h>
int main()
{
 
 float vetor[7] = {2.7, 6, 1.2, 4, 3.5, 9.9, 5.2};
 float A = vetor[0];
 float B = vetor[0];
 
 for (int i=1; i<7; i++) {
     if (vetor[i] > A)
     A = vetor[i];
     
     if (vetor[i] < B)
     B = vetor[i];
 }
 printf("\n\n %.1f e %.1f", A, B);
}

